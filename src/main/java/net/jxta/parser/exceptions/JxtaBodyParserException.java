package net.jxta.parser.exceptions;

public class JxtaBodyParserException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JxtaBodyParserException() {
		super();
	}


	public JxtaBodyParserException(String message) {
		super(message);
	}


	public JxtaBodyParserException(String message, Throwable cause) {
		super(message, cause);
	}


	public JxtaBodyParserException(Throwable cause) {
		super(cause);
	}

}
