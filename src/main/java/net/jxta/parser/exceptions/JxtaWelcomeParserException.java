package net.jxta.parser.exceptions;

public class JxtaWelcomeParserException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JxtaWelcomeParserException() {
		super();
	}


	public JxtaWelcomeParserException(String message) {
		super(message);
	}


	public JxtaWelcomeParserException(String message, Throwable cause) {
		super(message, cause);
	}


	public JxtaWelcomeParserException(Throwable cause) {
		super(cause);
	}

}
