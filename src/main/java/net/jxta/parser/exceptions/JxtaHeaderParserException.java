package net.jxta.parser.exceptions;

public class JxtaHeaderParserException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JxtaHeaderParserException() {
		super();
	}


	public JxtaHeaderParserException(String message) {
		super(message);
	}


	public JxtaHeaderParserException(String message, Throwable cause) {
		super(message, cause);
	}


	public JxtaHeaderParserException(Throwable cause) {
		super(cause);
	}

}
